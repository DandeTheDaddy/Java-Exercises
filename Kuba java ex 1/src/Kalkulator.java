public class Kalkulator {
    static int metodaDodawanie(int a, int b) {
        return (a + b);
    }

    static int metodaOdejmowanie(int a, int b) {
        return (a - b);
    }

    static int metodaMnozenie(int a, int b) {
        return (a * b);
    }

    static int metodaDzielenie(int a, int b) {
        return (a / b);
    }

}
